// 전체 타로 카드 데이터
export const ALL_TAROT_CARDS: TarotCard[] = [
  ...MAJOR_ARCANA,
  ...WANDS,
  ...CUPS,
  ...SWORDS,
  ...PENTACLES,
];

// ID로 카드 찾기
export function getCardById(id: string): TarotCard | undefined {
  return ALL_TAROT_CARDS.find((card) => card.id === id);
}

// 슈트로 카드 필터링
export function getCardsBySuit(suit: TarotSuit): TarotCard[] {
  return ALL_TAROT_CARDS.filter((card) => card.suit === suit);
}

// 랜덤 카드 뽑기
export function drawRandomCard(): TarotCard {
  const randomIndex = Math.floor(Math.random() * ALL_TAROT_CARDS.length);
  return ALL_TAROT_CARDS[randomIndex];
}

// 여러 장의 랜덤 카드 뽑기 (중복 없음)
export function drawMultipleCards(count: number): TarotCard[] {
  const shuffled = [...ALL_TAROT_CARDS].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
